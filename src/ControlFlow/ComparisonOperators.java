package ControlFlow;

public class ComparisonOperators {
    public static void main(String[] args) {
        int x=1;
        int y=2;
        System.out.println(x == y); //Boolean expression it produce true or false
        System.out.println(x != y);
        System.out.println(x >= y);
        System.out.println(x <= y);
    }
}
